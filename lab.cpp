#include <iostream>
using namespace std;

int main() {
    int A, B;

    // Assign a value to A
    A = 100;

    // Subtract 98 from A and store in B
    B = A - 98;

    // Add 1 to A and store back in A
    A = A + 1;

    // Output results
    cout << "Value of A: " << A << endl;
    cout << "Value of B: " << B << endl;

    return 0;
}